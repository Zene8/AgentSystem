#!/usr/bin/env node
// generate-routing-table.js — #124: regenerates the routing table in .agents/agents/jarvis.md
// from config/routing.yml (the single source of truth, also consumed live by
// hooks/memory-router.js via hooks/routing-config.js), so the two can never drift apart again.
// Idempotent: the generated rows live between HTML comment markers and are fully replaced on
// every run. If the markers are missing (e.g. first run against an old jarvis.md), they are
// inserted right after the table's header/separator row.
//
// Usage:
//   node tools/generate-routing-table.js [configPath] [jarvisMdPath]

import { readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { createRequire } from 'node:module';

// hooks/routing-config.js is CommonJS (shared with the CJS-only hook harness) — bridge via
// createRequire rather than duplicating its parsing logic in ESM.
const require = createRequire(import.meta.url);
const __dirname = dirname(fileURLToPath(import.meta.url));
const { loadRoutingRules, defaultConfigPath } = require('../hooks/routing-config.js');

export const JARVIS_MD_PATH = join(__dirname, '..', '.agents', 'agents', 'jarvis.md');
export const START_MARKER = '<!-- ROUTING_TABLE:START (generated by tools/generate-routing-table.js from config/routing.yml — do not edit rows below by hand) -->';
export const END_MARKER = '<!-- ROUTING_TABLE:END -->';

// Pure: render one routing.yml rule as a markdown table row matching jarvis.md's column order
// (Task signal | Route to | Command).
export function renderRow(rule) {
  return `  | ${rule.keywordsDisplay} | **${rule.agentShort}** | \`${rule.command}\` |`;
}

// Pure: render all rules as the markdown table body (one row per rule, in routing.yml order).
export function renderTable(rules) {
  return rules.map(renderRow).join('\n');
}

// Pure: splice `tableBody` between START_MARKER/END_MARKER in `content`. If the markers are
// already present, replaces everything between them. If absent, inserts a fresh marker pair
// immediately after the table's header separator row (the "|---|---|---|" line following the
// "Task signal" header) — self-healing so a hand-edited jarvis.md doesn't need manual marker
// placement before the first regeneration.
export function spliceTable(content, tableBody) {
  const startIdx = content.indexOf(START_MARKER);
  const endIdx = content.indexOf(END_MARKER);
  if (startIdx !== -1 && endIdx !== -1 && endIdx > startIdx) {
    return content.slice(0, startIdx) + START_MARKER + '\n' + tableBody + '\n' + content.slice(endIdx);
  }

  const lines = content.split('\n');
  const headerIdx = lines.findIndex(l => /Task signal/.test(l));
  if (headerIdx === -1) {
    throw new Error('generate-routing-table: could not find routing table header ("Task signal") in jarvis.md');
  }
  const sepIdx = headerIdx + 1; // separator row "|---|---|---|" immediately follows the header
  const before = lines.slice(0, sepIdx + 1).join('\n');
  const after = lines.slice(sepIdx + 1).join('\n');
  return `${before}\n${START_MARKER}\n${tableBody}\n${END_MARKER}\n${after}`;
}

// Guard against #154: an unrecognized flag (typo'd or new) being silently swallowed as a
// positional configPath/jarvisPath argument. loadRoutingRules() would then read a bogus path,
// return [] (no rules), and generate() would happily overwrite jarvis.md with an EMPTY routing
// table — wiping every routing rule with no error. Refusing to write when rules.length === 0 is
// the real fix; the flag allowlist is defense-in-depth so a typo'd flag fails fast with a usage
// message instead of ever reaching that code path.
export class EmptyRoutingRulesError extends Error {}

export function generate(configPath, jarvisPath, { allowEmpty = false, dryRun = false } = {}) {
  const rules = loadRoutingRules(configPath || defaultConfigPath());
  if (rules.length === 0 && !allowEmpty) {
    throw new EmptyRoutingRulesError(
      `generate-routing-table: loadRoutingRules() returned 0 rules from ` +
      `"${configPath || defaultConfigPath()}" — refusing to write an empty routing table. ` +
      `Check that the config path is correct and the file has routing rules.`
    );
  }
  const tableBody = renderTable(rules);
  const targetPath = jarvisPath || JARVIS_MD_PATH;
  const content = readFileSync(targetPath, 'utf8');
  const updated = spliceTable(content, tableBody);
  if (!dryRun) {
    writeFileSync(targetPath, updated, 'utf8');
  }
  return { rulesCount: rules.length, path: targetPath, content: updated, changed: content !== updated };
}

const isMain = process.argv[1] &&
  process.argv[1].replace(/\\/g, '/') === fileURLToPath(import.meta.url).replace(/\\/g, '/');

const USAGE = `Usage: node tools/generate-routing-table.js [--check] [--config <path>] [--jarvis-md <path>]

  --check          Dry-run: report what would change without writing.
  --config PATH    Path to routing.yml (default: hooks/routing-config.js's defaultConfigPath()).
  --jarvis-md PATH Path to jarvis.md to update (default: .agents/agents/jarvis.md).
  -h, --help       Show this message.`;

// Strict allowlisted flag parser — any unrecognized flag exits loudly (exit 2) instead of being
// silently consumed as a positional path argument (the root cause of #154).
export function parseArgs(argv) {
  const flags = { check: false, config: null, jarvisMd: null, help: false };
  const KNOWN = new Set(['--check', '--config', '--jarvis-md', '-h', '--help']);
  for (let i = 0; i < argv.length; i++) {
    const arg = argv[i];
    if (!KNOWN.has(arg)) {
      throw new Error(`Unknown flag: "${arg}"\n\n${USAGE}`);
    }
    if (arg === '--check') flags.check = true;
    else if (arg === '-h' || arg === '--help') flags.help = true;
    else if (arg === '--config') {
      const val = argv[++i];
      if (val === undefined) throw new Error(`--config requires a path argument\n\n${USAGE}`);
      flags.config = val;
    } else if (arg === '--jarvis-md') {
      const val = argv[++i];
      if (val === undefined) throw new Error(`--jarvis-md requires a path argument\n\n${USAGE}`);
      flags.jarvisMd = val;
    }
  }
  return flags;
}

if (isMain) {
  let flags;
  try {
    flags = parseArgs(process.argv.slice(2));
  } catch (err) {
    console.error(err.message);
    process.exit(2);
  }

  if (flags.help) {
    console.log(USAGE);
    process.exit(0);
  }

  try {
    const result = generate(flags.config, flags.jarvisMd, { dryRun: flags.check });
    if (flags.check) {
      console.log(result.changed
        ? `generate-routing-table --check: jarvis.md is OUT OF DATE (${result.rulesCount} rule(s) would be written to ${result.path})`
        : `generate-routing-table --check: jarvis.md is up to date (${result.rulesCount} rule(s))`);
      process.exit(result.changed ? 1 : 0);
    }
    console.log(`generate-routing-table: wrote ${result.rulesCount} row(s) to ${result.path}`);
  } catch (err) {
    console.error(err.message);
    process.exit(err instanceof EmptyRoutingRulesError ? 1 : 2);
  }
}
